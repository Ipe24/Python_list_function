# List
# Built in function

# list.sort()
num = [5,1,4,9,3]       # List of number
num.sort()              # Sort the item by default
print(num)              # Output = [1, 3, 4, 5, 9]
print()

num = [5,1,4,9,3]       # List of number
num.sort(reverse=True)  # Sort the item by using reverse function
print(num)              # Output = [9, 5, 4, 3, 1]
print()