# List
# Built in function

# list.pop(index)
num = [1,2,3]           # List of number
num.pop(0)              # Remove the item in that index
print(num)              # Output = [2,3]
print()

num = [1,2,3]           # List of number
num.pop()              # Remove the last item in that list if there is no index given
print(num)              # Output = [1,2]
print()