# list.append(item)
num=[1,2,3,4]      # list of number
num.append(3)      # Added the item in the last part of the list
print(num)         # Output = [1,2,3,4,3]
print()

num2=[-1,-2,-3]    # Another list of negative number
num.append(num2)   # Add the lit inside the list
print(num)         #Output = [1,2,3,4,3,[-1,-2,-3]]
print()

